# Systemic initialization constants (D-vectors) used to bind keys
D_CONST = [
    0x44D7, 0x26BC, 0x626B, 0x135E, 0x5789, 0x35E2, 0x7135, 0x09AF,
    0x4D78, 0x2BC6, 0x26BC, 0x135E, 0x5789, 0x35E2, 0x7135, 0x09AF
]

class ZucEngine:
    def __init__(self, key_str):
        # Derive a uniform 128-bit key block and IV from the master password hash
        h = hashlib.sha256(key_str.encode('utf-8')).digest()
        K = list(h[:16])
        IV = list(h[16:32])
        
        # 1. Initialize the 16 LFSR state cells (31-bit bounds)
        self.LFSR = [0] * 16
        for i in range(16):
            self.LFSR[i] = (K[i] << 23) | (D_CONST[i] << 8) | IV[i]
            
        # 2. Initialize the internal non-linear registers
        self.R1 = 0
        self.R2 = 0
        
        # Run 32 preliminary warm-up initialization cycles
        for _ in range(32):
            W = self._clock_f()
            self._clock_lfsr_init(W >> 1)

    def _clock_lfsr_init(self, u):
        # 31-bit Galois Field primitive polynomial mixing function
        v = (self.LFSR[0] << 8) ^ (self.LFSR[4] << 20) ^ (self.LFSR[10] << 21) ^ (self.LFSR[13] << 17) ^ self.LFSR[15]
        v = (v & 0x7FFFFFFF) + (v >> 31)
        v = (v + u) & 0x7FFFFFFF
        v = (v & 0x7FFFFFFF) + (v >> 31)
        
        # Shift the stack forward
        self.LFSR.pop(0)
        self.LFSR.append(v)

    def _clock_lfsr_work(self):
        v = (self.LFSR[0] << 8) ^ (self.LFSR[4] << 20) ^ (self.LFSR[10] << 21) ^ (self.LFSR[13] << 17) ^ self.LFSR[15]
        v = (v & 0x7FFFFFFF) + (v >> 31)
        
        self.LFSR.pop(0)
        self.LFSR.append(v)

    def _clock_f(self):
        # Bit Reconstruction Layer
        X0 = ((self.LFSR[15] & 0x7FFF) << 16) | (self.LFSR[14] & 0xFFFF)
        X1 = ((self.LFSR[11] & 0xFFFF) << 16) | (self.LFSR[9] & 0x7FFF)
        X2 = ((self.LFSR[7] & 0xFFFF) << 16) | (self.LFSR[5] & 0x7FFF)
        
        # Non-Linear Matrix S-Box layer transform
        W = (X0 ^ self.R1) + self.R2
        W_out = W & 0xFFFFFFFF
        
        W1 = self.R1 + X1
        W2 = self.R2 ^ X2
        
        # Pass elements through cascading S-Boxes
        u = (S0[(W1 >> 24) & 0xFF] << 24) | (S1[(W1 >> 16) & 0xFF] << 16) | (S0[(W1 >> 8) & 0xFF] << 8) | S1[W1 & 0xFF]
        v = (S1[(W2 >> 24) & 0xFF] << 24) | (S0[(W2 >> 16) & 0xFF] << 16) | (S1[(W2 >> 8) & 0xFF] << 8) | S0[W2 & 0xFF]
        
        # Linear transformations update tracking registers
        self.R1 = u ^ ((v << 2) | (v >> 30)) ^ ((v << 10) | (v >> 22)) ^ ((v << 18) | (v >> 14)) ^ ((v << 24) | (v >> 8))
        self.R1 &= 0xFFFFFFFF
        self.R2 = v ^ ((u << 8) | (u >> 24)) ^ ((u << 14) | (u >> 18)) ^ ((u << 22) | (u >> 10)) ^ ((u << 30) | (u >> 2))
        self.R2 &= 0xFFFFFFFF
        
        return W_out

    def generate_keystream_word(self):
        # Bit Reconstruction for active keystream word extraction
        X3 = ((self.LFSR[2] & 0xFFFF) << 16) | (self.LFSR[0] & 0x7FFF)
        W = self._clock_f()
        self._clock_lfsr_work()
        return (W ^ X3) & 0xFFFFFFFF

def zuc_crypt(data_bytes, key_str):
    engine = ZucEngine(key_str)
    result = bytearray()
    
    # Process data in 4-byte chunks (32-bit words)
    for idx in range(0, len(data_bytes), 4):
        chunk = data_bytes[idx:idx+4]
        k_word = engine.generate_keystream_word()
        
        # Unpack keystream into 4 bytes
        k_bytes = struct.pack(">I", k_word)
        
        # XOR data stream with the key byte elements
        for i, b in enumerate(chunk):
            result.append(b ^ k_bytes[i])
            
    return bytes(result)

def encrypt(text, key):
    try:
        if not text: return ""
        data = text.encode('utf-8')
        encrypted_bytes = zuc_crypt(data, str(key))
        return base64.b64encode(encrypted_bytes).decode('utf-8')
    except Exception as e:
        return f"ZUC Error: {str(e)}"

def decrypt(text, key):
    try:
        if not text: return ""
        data = base64.b64decode(text.encode('utf-8'))
        decrypted_bytes = zuc_crypt(data, str(key))
        
        # Safely capture clean terminal characters without dropping trailing bytes
        return decrypted_bytes.decode('utf-8', errors='replace').rstrip('\x00')
    except Exception as e:
        return f"ZUC Decrypt Error: {str(e)}"